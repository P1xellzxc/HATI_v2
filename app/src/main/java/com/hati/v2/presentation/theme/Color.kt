package com.hati.v2.presentation.theme

import androidx.compose.ui.graphics.Color

/**
 * HATI_v2 Manga Finance Color System.
 * Strictly monochrome: Black (#000000) / White (#FFFFFF).
 */
object MangaColors {
    val Black = Color(0xFF000000)
    val White = Color(0xFFFFFFFF)
    
    // Semantic aliases
    val Primary = Black
    val OnPrimary = White
    val Background = White
    val OnBackground = Black
    val Surface = White
    val OnSurface = Black
    val Border = Black
    val Shadow = Black
}
